import math

pi = 3.14

print(math.pi)
print(math.e)

print(round(pi)) # round to nearest integer
print(abs(pi)) # absolute value
print(pow(pi, 2)) # power
print(max(1, 2, 3, 4, 5)) # max value
print(min(1, 2, 3, 4, 5)) # min value

print(math.ceil(pi)) # round up
print(math.floor(pi)) # round down
print(math.sqrt(pi)) # square root
print(math.sin(pi / 2)) # sine